interface I18nContextValue {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: string, fallback?: string) => string;
  isAutoDetected: boolean;
  browserLanguageInfo: ReturnType<typeof getBrowserLanguageInfo>;
  enableAutoDetection: () => void;
}

const I18nContext = createContext<I18nContextValue | undefined>(undefined);

export const I18nProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [lang, setLangState] = useState<Lang>('tr');
  const [isAutoDetected, setIsAutoDetected] = useState(false);
  const [browserLanguageInfo] = useState(() => getBrowserLanguageInfo());

  // Initialize language on first load
  useEffect(() => {
    const saved = localStorage.getItem('appLang') as Lang | null;
    
    console.log('[I18n] Initializing language. Saved:', saved);
    console.log('[I18n] Browser language info:', browserLanguageInfo);
    
    if (shouldUseBrowserDetection(saved)) {
      // Use browser detection for new users or when explicitly set to auto
      const detectedLang = detectBrowserLanguage();
      console.log('[I18n] Using browser detection, detected:', detectedLang);
      setLangState(detectedLang);
      setIsAutoDetected(true);
      // Save the detected language so we don't re-detect on every load
      try { 
        localStorage.setItem('appLang', detectedLang);
        localStorage.setItem('appLangAutoDetected', 'true');
      } catch {}
    } else if (saved === 'en' || saved === 'tr') {
      // Use saved preference
      console.log('[I18n] Using saved preference:', saved);
      setLangState(saved);
      setIsAutoDetected(localStorage.getItem('appLangAutoDetected') === 'true');
    } else {
      // Fallback for existing users without saved preference
      console.log('[I18n] Fallback to Turkish for existing users');
      setLangState('tr');
      setIsAutoDetected(false);
    }
  }, []);

  const setLang = (l: Lang) => {
    console.log('[I18n] Manually setting language to:', l);
    setLangState(l);
    setIsAutoDetected(false);
    try { 
      localStorage.setItem('appLang', l); 
      localStorage.setItem('appLangAutoDetected', 'false');
    } catch {}
  };

  // Function to enable auto-detection
  const enableAutoDetection = () => {
    console.log('[I18n] Enabling auto-detection');
    const detectedLang = detectBrowserLanguage();
    setLangState(detectedLang);
    setIsAutoDetected(true);
    try {
      localStorage.setItem('appLang', detectedLang);
      localStorage.setItem('appLangAutoDetected', 'true');
    } catch {}
  };

  const t = (key: string, fallback?: string) => {
    const table = dict[lang] || {};
    if (key in table) return table[key];
    // Do not force Turkish fallback when another language is selected.
    return fallback ?? key;
  };

  const value = useMemo(() => ({ 
    lang, 
    setLang, 
    t, 
    isAutoDetected, 
    browserLanguageInfo,
    enableAutoDetection 
  }), [lang, isAutoDetected, browserLanguageInfo]);
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
};

export function useI18n(): I18nContextValue {
  const ctx = useContext(I18nContext);
  if (!ctx) {
    console.error('useI18n must be used within I18nProvider');
    // Return a safe fallback during hot reload
    return {
      lang: 'tr',
      setLang: () => {},
      t: (key: string, fallback?: string) => fallback ?? key,
      isAutoDetected: false,
      browserLanguageInfo: { language: '', languages: [], detected: 'tr' },
      enableAutoDetection: () => {}
    };
  }
  return ctx;
}
